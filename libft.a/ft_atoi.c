int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	sum;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	sign = 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	sum = 0;
	while ((str[i] >= 48 && str[i] <= 57))
	{
		sum = sum * 10 + (str[i] - 48);
		i++;
	}
	return (sum * sign);
}
