#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*chr;
	size_t			i;

	i = 0;
	chr = (unsigned char *)s;
	while (i < n)
	{
		chr[i] = '\0';
		i++;
	}
}
